const LABELS = ['А', 'Б', 'В', 'Г']
const TOTAL = 20

function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

export default function Quiz() {
  const [questions, setQuestions] = useState([])
  const [current, setCurrent]     = useState(0)
  const [selected, setSelected]   = useState(null)
  const [confirmed, setConfirmed] = useState(false)
  const [answers, setAnswers]     = useState([]) // {correct, selected, question, opts, correctIdx}
  const [finished, setFinished]   = useState(false)

  useEffect(() => {
    setQuestions(shuffle(ALL_QUESTIONS).slice(0, TOTAL))
  }, [])

  if (!questions.length) return null

  const q = questions[current]
  const score = answers.filter(a => a.correct).length

  const handleConfirm = () => {
    if (selected === null) return
    const correct = selected === q.a
    setConfirmed(true)
    setAnswers(prev => [...prev, { correct, selected, question: q.q, opts: q.opts, correctIdx: q.a }])
  }

  const handleNext = () => {
    if (current + 1 >= TOTAL) {
      setFinished(true)
    } else {
      setCurrent(c => c + 1)
      setSelected(null)
      setConfirmed(false)
    }
  }

  const handleRestart = () => {
    setQuestions(shuffle(ALL_QUESTIONS).slice(0, TOTAL))
    setCurrent(0)
    setSelected(null)
    setConfirmed(false)
    setAnswers([])
    setFinished(false)
  }

  // ── ЭКРАН РЕЗУЛЬТАТОВ ──
  if (finished) {
    const pct = Math.round((score / TOTAL) * 100)
    const passed = score >= TOTAL * 0.8
    const mistakes = answers.filter(a => !a.correct)

    return (
      <main className="quiz-page">
        <div className="container quiz-container">
          <div className="quiz-result-header">
            <img src="/logo.png" alt="Тайпан" className="quiz-logo" />
            <h1 className="quiz-title">РЕЗУЛЬТАТ</h1>
            <div className={`quiz-score-big ${passed ? 'quiz-score-pass' : 'quiz-score-fail'}`}>
              {score} / {TOTAL}
            </div>
            <div className="quiz-score-pct">{pct}%</div>
            <p className={`quiz-verdict ${passed ? 'quiz-verdict-pass' : 'quiz-verdict-fail'}`}>
              {passed
                ? 'Отличный результат! Вы готовы к аттестации.'
                : 'Есть над чем поработать. Повторите материал и попробуйте снова.'}
            </p>
          </div>

          {mistakes.length > 0 && (
            <div className="quiz-mistakes">
              <p className="quiz-mistakes-title">Разбор ошибок</p>
              {mistakes.map((m, i) => (
                <div key={i} className="quiz-mistake-item">
                  <div className="quiz-mistake-q">{m.question}</div>
                  <div className="quiz-mistake-your">
                    <span className="quiz-tag quiz-tag-wrong">Ваш ответ:</span> {m.opts[m.selected]}
                  </div>
                  <div className="quiz-mistake-correct">
                    <span className="quiz-tag quiz-tag-right">Правильно:</span> {m.opts[m.correctIdx]}
                  </div>
                </div>
              ))}
            </div>
          )}

          <div className="quiz-result-btns">
            <button className="btn-primary" onClick={handleRestart}>Пройти ещё раз</button>
            <Link to="/apply" className="btn-outline">Записаться в клуб</Link>
          </div>
        </div>
      </main>
    )
  }

  // ── ЭКРАН ВОПРОСА ──
  return (
    <main className="quiz-page">
      <div className="container quiz-container">

        <div className="quiz-header">
          <img src="/logo.png" alt="Тайпан" className="quiz-logo" />
          <div>
            <p className="section-label">Клуб тхэквондо Тайпан · Павловский Посад</p>
            <h1 className="quiz-title">ПРОВЕРЬ СВОИ ЗНАНИЯ</h1>
            <p className="quiz-subtitle">Программа аттестации 10–8 гыпы</p>
          </div>
        </div>

        <div className="quiz-progress-wrap">
          <div className="quiz-progress-bar">
            <div className="quiz-progress-fill" style={{ width: `${((current) / TOTAL) * 100}%` }} />
          </div>
          <span className="quiz-counter">Вопрос {current + 1} из {TOTAL}</span>
        </div>

        <div className="quiz-card">
          <div className="quiz-question">{q.q}</div>
          <div className="quiz-options">
            {q.opts.map((opt, i) => {
              let cls = 'quiz-option'
              if (confirmed) {
                if (i === q.a) cls += ' quiz-option-correct'
                else if (i === selected) cls += ' quiz-option-wrong'
              } else if (i === selected) {
                cls += ' quiz-option-selected'
              }
              return (
                <button
                  key={i}
                  className={cls}
                  onClick={() => !confirmed && setSelected(i)}
                  disabled={confirmed}
                >
                  <span className="quiz-option-label">{LABELS[i]}</span>
                  <span className="quiz-option-text">{opt}</span>
                </button>
              )
            })}
          </div>

          {confirmed && (
            <div className={`quiz-feedback ${answers[answers.length - 1].correct ? 'quiz-feedback-correct' : 'quiz-feedback-wrong'}`}>
              {answers[answers.length - 1].correct ? 'Верно!' : `Неверно. Правильный ответ: ${q.opts[q.a]}`}
            </div>
          )}

          <div className="quiz-actions">
            {!confirmed
              ? <button className="btn-primary" onClick={handleConfirm} disabled={selected === null}>Ответить</button>
              : <button className="btn-primary" onClick={handleNext}>{current + 1 >= TOTAL ? 'Завершить тест' : 'Следующий вопрос'}</button>
            }
          </div>
        </div>

        <div className="quiz-footer-note">
          Тест предназначен для учеников, готовящихся к аттестации на 10–8 гыпы.
          Из общего банка вопросов случайно выбираются 20.
          <Link to="/apply"> Хотите заниматься в нашем клубе?</Link>
        </div>

      </div>
    </main>
  )
}
