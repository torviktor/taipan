-- Миграция: добавить фото в слоты «Лучшие сезона» (season_best_athletes).
--
-- Первые 4 рамки Зала Славы (абсолютные чемпионы / лучшие по результатам
-- сезона) получают возможность хранить фото на весь сезон. Поля повторяют
-- механизм Зала Славы (HallOfFame.photo_url / photo_position).
--
-- photo_position хранится в формате "Xpx Ypx / zoom%" — том же, что парсит
-- ChampionImg на фронте. Дефолт "0px 0px / 100%" = фото по центру, 100%.
--
-- create_all() при старте бэка добавит эти колонки на пустых БД (модель
-- обновлена), но на проде с уже наполненной БД эти ALTER нужно прогнать вручную.
--
-- Применять командами раздельно через psql.

-- ─────────────────────────────────────────────────────────────────────────────
-- ШАГ 1. Добавить колонки.
-- ─────────────────────────────────────────────────────────────────────────────
ALTER TABLE season_best_athletes ADD COLUMN photo_url      VARCHAR(500);
ALTER TABLE season_best_athletes ADD COLUMN photo_position VARCHAR(50);

-- ─────────────────────────────────────────────────────────────────────────────
-- ШАГ 2. Проставить дефолт позиции существующим записям (если есть),
-- чтобы переиспользуемый рендер ChampionImg отрисовал фото корректно
-- до ручной настройки кадра. NULL допустим (фронт сам подставит фолбэк),
-- но единообразие удобнее.
-- ─────────────────────────────────────────────────────────────────────────────
UPDATE season_best_athletes SET photo_position = '0px 0px / 100%' WHERE photo_position IS NULL;

