-- Миграция: UNIQUE-индекс на (athlete_id, period_year, period_month)
-- в таблице athlete_fee_periods.
--
-- Цель: защита от race condition при параллельном вызове POST /fees/periods/init
-- двумя менеджерами — без индекса в БД появляются дубликаты записей с одинаковым
-- (athlete_id, period_year, period_month).
--
-- ВНИМАНИЕ: выполнять две команды раздельно.
-- Сначала ШАГ 1 (диагностика) — он должен вернуть 0 строк.
-- Если строки есть — на проде уже есть дубликаты, индекс не создастся,
-- сначала нужно почистить (решим вручную).
-- Только после успешного ШАГА 1 — выполнять ШАГ 2 (DDL).

-- ─────────────────────────────────────────────────────────────────────────────
-- ШАГ 1. Диагностика: должно вернуть 0 строк.
-- ─────────────────────────────────────────────────────────────────────────────
SELECT athlete_id, period_year, period_month, COUNT(*) AS cnt
FROM athlete_fee_periods
GROUP BY athlete_id, period_year, period_month
HAVING COUNT(*) > 1;

-- ─────────────────────────────────────────────────────────────────────────────
-- ШАГ 2. Создание индекса (только если ШАГ 1 вернул 0 строк).
-- ─────────────────────────────────────────────────────────────────────────────
CREATE UNIQUE INDEX IF NOT EXISTS ux_afp_athlete_period
ON athlete_fee_periods (athlete_id, period_year, period_month);
